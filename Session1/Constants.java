
public class Constants {

	public static void main(String[] args) {
		
		/*
		 * 	This is to demonstrate a multi line comment
		 * In this program
		 * we will see
		 * 		- literals
		 * 		- final
		 */
		
		int salary = 50000; // 50000 is a literal i.e  data i.e value i.e constant
		salary = 75000; // salary is a variable but literal 75000 is a constant
		
		// final keyword in java makes the storage conatiner read only
		final int promoCode = 123321;
		//promoCode = 1223442; // error : now promoCode can not be modified :)
	}

}
